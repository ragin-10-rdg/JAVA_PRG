package week1;
public class operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 0;
        b = ++a; // Pre-increment
        System.out.println(b);
        System.out.println(a);

        int c = 20;
        int d = 0;
        d = c++; // Post-increment
        System.out.println(d);
        System.out.println(c);
    }
}

